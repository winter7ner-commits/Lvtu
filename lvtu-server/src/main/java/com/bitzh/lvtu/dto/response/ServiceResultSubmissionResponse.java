package com.bitzh.lvtu.dto.response;

import java.time.LocalDateTime;
import java.util.List;

public class ServiceResultSubmissionResponse {
    private Long id;
    private Long orderId;
    private Long resultId;
    private Long lawyerId;
    private Integer submissionNo;
    private String title;
    private String content;
    private LocalDateTime createdAt;
    private List<AttachmentResponse> attachments;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getOrderId() {
        return orderId;
    }

    public void setOrderId(Long orderId) {
        this.orderId = orderId;
    }

    public Long getResultId() {
        return resultId;
    }

    public void setResultId(Long resultId) {
        this.resultId = resultId;
    }

    public Long getLawyerId() {
        return lawyerId;
    }

    public void setLawyerId(Long lawyerId) {
        this.lawyerId = lawyerId;
    }

    public Integer getSubmissionNo() {
        return submissionNo;
    }

    public void setSubmissionNo(Integer submissionNo) {
        this.submissionNo = submissionNo;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public LocalDateTime getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(LocalDateTime createdAt) {
        this.createdAt = createdAt;
    }

    public List<AttachmentResponse> getAttachments() {
        return attachments;
    }

    public void setAttachments(List<AttachmentResponse> attachments) {
        this.attachments = attachments;
    }
}
